"""celeborn_cmm — codebase-memory-mcp (CMM) adapter: Sprint 1 "Zero Prompts".

Stdlib-only. This is the ENTIRE Celeborn-owned seam to CMM (the spec's §4 "adapter, not a
dependency"). Everything here is interface-level glue — it depends on CMM's public surface (its
14 MCP tool names + binary CLI) and NEVER on its internals. No code from the CMM tree is touched.

★ NORTH STAR (carry into every decision): we integrate CMM **first and foremost to preserve
vibe-coder FLOW by eliminating permission-prompt interruptions.** Token savings and code-
intelligence are real but SECONDARY. Sprint 1's job is to make structural questions answerable
through CMM's pre-cleared read-only tools with ZERO "Allow" clicks — and to *install* that
flow-first statement into the project's agent-facing instructions, not just document it here.

What S1 ships (CMM-1…CMM-5):
  • merge_cmm_permissions  — the pure, idempotent permission merge (the whole feature's safety).
  • schema_guard           — validate before writing; never emit a malformed settings file.
  • MCP-registration ensure — make CMM's server entry exist in the MCP channel (`.mcp.json`).
  • `celeborn cmm engage`  — orchestrate the above + first index + install the North Star block.
  • `celeborn cmm off`/`status` — reversible, sticky opt-out + honest readout.

Two channels, never mixed (Decision 3): the portable permission allow-list → project
`.claude/settings.json` (shareable); the machine-specific MCP server registration (binary path)
→ `.mcp.json`. We never write a binary path into settings.json.

Imported lazily by celeborn.py so the free, local core stays dependency- and network-free.
"""

