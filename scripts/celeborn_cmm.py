CMM_ALL_TOOLS = tuple(t for t in CMM_ALLOW_TOOLS if t.startswith(CMM_MCP_PREFIX)) + CMM_ASK_TOOLS

CMM_SERVER_NAME = "codebase-memory-mcp"
RC_KEY = "cmm"  # the .celebornrc block this module owns

