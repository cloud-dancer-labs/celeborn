def cmd_cmm(args):
    """Dispatch `celeborn cmm <engage|off|status>`."""
    action = getattr(args, "cmm_cmd", None)
    if action == "engage":
        return _cmd_engage(args)
    if action == "off":
        return _cmd_off(args)
    if action == "status":
        return _cmd_status(args)
    cb.die(f"unknown cmm command: {action!r}")


def _cmd_engage(args):
    ctx = cb.require_context(args)
    root = ctx.parent
    adapter = cb.active_adapter(ctx, getattr(args, "harness", None))
    global_ = getattr(args, "global_", False)
    state = _cmm_state(ctx)

    if state.get("opted_out") and not getattr(args, "force", False):
        cb.warn("CMM is opted out for this project (`celeborn cmm off` set a sticky marker). "
                "Re-engage with: celeborn cmm engage --force")
        return

    skipped: list[str] = []

    # CMM-1 (gated by CMM-2): the permission pre-clear — the headline. This must land even if the
    # rest degrades (Decision 6).
    perm = apply_permissions(ctx, adapter, global_=global_)
    if perm["status"] in ("applied", "noop"):
        # Record provenance only on a real apply; preserve prior provenance on a noop re-run.
        if perm["status"] == "applied":
            state["added_allow"] = perm["provenance"].get("allow_added", [])
            state["added_ask"] = perm["provenance"].get("ask_added", [])
        cb.ok(f"Permission pre-clear {'applied' if perm['status']=='applied' else 'already present'}: "
              f"{perm['target']}")
    else:
        skipped.append(f"permission pre-clear ({perm['reason']})")
        cb.warn(f"Permission pre-clear skipped — {perm['reason']}")

    # S2 (CMM-6): auto-provision the pinned binary so engage needs no manual install. Best-effort
    # and gated — `--no-provision` opts out, and any failure degrades (Decision 6 / CMM-10): the
    # permission pre-clear above has already landed, so the session is never blocked.
    if not getattr(args, "no_provision", False) and cmm_binary() is None:
        try:
            prov = __import__("celeborn_cmm_provision").provision()
        except Exception as e:  # noqa: BLE001 — provisioning must never break engage
            prov = {"status": "error", "reason": f"{type(e).__name__}: {e}"}
        if prov["status"] in ("provisioned", "cached"):
            cb.ok(f"CMM binary {'provisioned' if prov['status']=='provisioned' else 'present'} "
                  f"({prov.get('version')} · {prov.get('path')})")
        else:
            skipped.append(f"provision ({prov['reason']})")
            cb.warn(f"Auto-provision skipped — {prov['reason']}")

    # CMM-3: MCP server registration (the machine-specific channel).
    mcp = ensure_mcp_registration(ctx)
    if mcp["status"] in ("registered", "noop"):
        cb.ok(f"MCP server '{CMM_SERVER_NAME}' {'registered' if mcp['status']=='registered' else 'present'} "
              f"({mcp['path']})")
    else:
        skipped.append(f"MCP registration ({mcp['reason']})")
        cb.warn(f"MCP registration skipped — {mcp['reason']}")

    # First structural index (best-effort; degrade if CMM absent).
    idx = index_repository(ctx)
    if idx["status"] == "indexed":
        cb.ok(f"Structural index started ({idx['reason']})")
        state["indexed"] = True
    else:
        skipped.append(f"index ({idx['reason']})")
        cb.warn(f"Index skipped — {idx['reason']}")

    # CMM-4: install the North Star into the project's agent-facing instructions.
    ns = install_north_star(root)
    if ns:
        cb.ok(f"North Star installed → {', '.join(ns)}")
    else:
        cb.info("North Star already present in agent instructions")

    # Persist engage state + provenance.
    state["engaged"] = True
    state["opted_out"] = False
    state["scope"] = "global" if global_ else "project"
    state["engaged_at"] = cb.now_iso()
    _save_cmm_state(ctx, state)

    # §8 one-line transparency notice.
    print("🏹 Celeborn —> This project's big enough that I've switched on code-structure memory "
          "(and cleared the related permission prompts) to keep you in flow.")
    if skipped:
        cb.info("Engaged with degraded parts (S1 assumes CMM is installed for the index/query half): "
                + "; ".join(skipped))


def _cmd_off(args):
    ctx = cb.require_context(args)
    root = ctx.parent
    adapter = cb.active_adapter(ctx, getattr(args, "harness", None))
    state = _cmm_state(ctx)
    global_ = state.get("scope") == "global" or getattr(args, "global_", False)

    provenance = {"allow_added": state.get("added_allow") or [],
                  "ask_added": state.get("added_ask") or []}
    rev = revert_permissions(ctx, adapter, provenance, global_=global_)
    if rev["status"] == "reverted":
        cb.ok(f"Reverted CMM permission entries in {rev['target']}")
    elif rev["status"] == "error":
        cb.warn(f"Permission revert skipped — {rev['reason']}")
    else:
        cb.info("No CMM permission entries to revert")

    removed = remove_north_star(root)
    if removed:
        cb.ok(f"Removed North Star block from {', '.join(removed)}")

    # Sticky per-project opt-out: survives re-runs; engage requires --force to override.
    state["engaged"] = False
    state["opted_out"] = True
    state["added_allow"] = []
    state["added_ask"] = []
    state["off_at"] = cb.now_iso()
    _save_cmm_state(ctx, state)
    cb.ok("CMM disengaged for this project (sticky opt-out set). Re-engage: celeborn cmm engage --force")


def _cmd_status(args):
    ctx = cb.require_context(args)
    adapter = cb.active_adapter(ctx, getattr(args, "harness", None))
    state = _cmm_state(ctx)
    global_ = state.get("scope") == "global" or getattr(args, "global_", False)

    binary = cmm_binary()
    target = _permission_target(ctx, adapter, global_=global_)
    allow_present = ask_present = False
    if target is not None:
        data = _read_json(target) or {}
        perms = data.get("permissions") or {}
        allow_set = set(perms.get("allow") or [])
        ask_set = set(perms.get("ask") or [])
        allow_present = all(t in allow_set for t in CMM_ALLOW_TOOLS)
        ask_present = all(t in ask_set for t in CMM_ASK_TOOLS)

    # S2: surface the pin + whether the binary is the auto-provisioned one (best-effort).
    pin_version = provisioned = None
    try:
        prov = __import__("celeborn_cmm_provision")
        pin_version = prov.load_pin().get("version")
        provisioned = prov.resolve_cached_binary()
    except Exception:
        pass

    doc = {
        "engaged": bool(state.get("engaged")),
        "opted_out": bool(state.get("opted_out")),
        "indexed": bool(state.get("indexed")),
        "scope": state.get("scope") or ("global" if global_ else "project"),
        "cmm_installed": binary is not None,
        "cmm_binary": binary,
        "pin_version": pin_version,
        "provisioned": provisioned is not None,
        "mcp_registered": mcp_registered(ctx),
        "allow_list_present": allow_present,
        "ask_list_present": ask_present,
        "permission_target": str(target) if target else None,
        "claude_version": detect_claude_version(),
        "engaged_at": state.get("engaged_at"),
    }
    if getattr(args, "json", False):
        print(json.dumps(doc, indent=2))
        return

    def yn(b):
        return "yes" if b else "no"

    print(f"🏹 Celeborn CMM status ({adapter.name})")
    print(f"  engaged:           {yn(doc['engaged'])}" + ("  ⛔ opted out" if doc["opted_out"] else ""))
    print(f"  CMM installed:     {yn(doc['cmm_installed'])}"
          + (f"  ({binary})" if binary else "  — `celeborn cmm provision` once the pin is finalized"))
    print(f"  pin / provisioned: {doc['pin_version'] or '—'} / {yn(doc['provisioned'])}")
    print(f"  MCP registered:    {yn(doc['mcp_registered'])}")
    print(f"  structure indexed: {yn(doc['indexed'])}")
    print(f"  allow-list ({len(CMM_ALLOW_TOOLS)}):   {yn(doc['allow_list_present'])}"
          + (f"   → {doc['permission_target']}" if doc["permission_target"] else "   (no per-command allow-list on this harness)"))
    print(f"  ask-list ({len(CMM_ASK_TOOLS)} mutating): {yn(doc['ask_list_present'])}")
    if doc["claude_version"]:
        print(f"  Claude Code:       {doc['claude_version']}")
    if not doc["cmm_installed"]:
        cb.info("CMM binary not found — the permission pre-clear still lands; install "
                "codebase-memory-mcp (or set $CELEBORN_CMM_BIN) for the index/query half. S2 automates this.")
