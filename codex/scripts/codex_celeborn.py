def agents_md_block(root: Path) -> str:
    """The managed Celeborn block Codex auto-loads from the repo's AGENTS.md every session."""
    return (
        f"{AGENTS_BEGIN}\n"
        "# Celeborn — long-term memory for this repo\n\n"
        "**Memory:** `.context/` in this repository (orient from `state.md` + `session.json`).\n\n"
        "## Every Codex session — orient FIRST\n\n"
        "Codex does not inject SessionStart hook stdout into the model. Orient is **your first\n"
        "action** before replying:\n\n"
        "1. If `.context/.codex-orient-pending.md` exists → read it once, orient, then **delete it**.\n"
        "2. Else → run `celeborn status` from this repository.\n\n"
        "## Kanban shorthand (this project only)\n\n"
        "| You say | Celeborn does |\n"
        "|---|---|\n"
        "| `wire tN` / `claim tN` | `celeborn claim tN --by <you>` then implement |\n"
        "| `ship tN` | `celeborn ship tN` |\n"
        "| `hydrate` / `orient` | Read the orient-pending file or run `celeborn status` |\n\n"
        "Cards live in `.context/tasks.md`.\n"
        f"{AGENTS_END}\n"
    )


def ensure_agents_md(root: Path) -> bool:
    """Write or refresh the managed Celeborn block in `<root>/AGENTS.md`, preserving any other
    content. Returns True when the file changed."""
    path = root / "AGENTS.md"
    block = agents_md_block(root)
    if path.is_file():
        existing = path.read_text(encoding="utf-8")
        if AGENTS_BEGIN in existing and AGENTS_END in existing:
            start = existing.index(AGENTS_BEGIN)
            stop = existing.index(AGENTS_END) + len(AGENTS_END)
            new = existing[:start] + block.rstrip("\n") + existing[stop:]
            if new == existing:
                return False
            path.write_text(new, encoding="utf-8")
            return True
        # Append below existing content.
        sep = "" if existing.endswith("\n\n") else ("\n" if existing.endswith("\n") else "\n\n")
        path.write_text(existing + sep + block, encoding="utf-8")
        return True
    path.write_text(block, encoding="utf-8")
    return True


# --------------------------------------------------------------------------- permission lever

def codex_config_path() -> Path:
    return codex_home() / "config.toml"


def codex_permission_status(root: Path) -> dict:
    """Read the coarse Codex permission lever from ~/.codex/config.toml WITHOUT a TOML parser
    (stdlib-only, tolerant of any layout): the global `approval_policy` / `sandbox_mode`, and whether
    THIS project is marked trusted via a `[projects."<root>"]` table with `trust_level = "trusted"`.
    Interactive (untrusted/on-request/on-failure) + not-trusted ⇒ the workspace still re-prompts."""
    cfg = codex_config_path()
    status = {"config": str(cfg), "exists": cfg.is_file(),
              "approval_policy": None, "sandbox_mode": None, "project_trusted": False}
    if not cfg.is_file():
        return status
    try:
        text = cfg.read_text(encoding="utf-8")
    except OSError:
        return status
    m = re.search(r'(?m)^\s*approval_policy\s*=\s*"([^"]+)"', text)
    if m:
        status["approval_policy"] = m.group(1)
    m = re.search(r'(?m)^\s*sandbox_mode\s*=\s*"([^"]+)"', text)
    if m:
        status["sandbox_mode"] = m.group(1)
    rp = str(root.resolve())
    # A [projects."<root>"] header followed (before the next table) by trust_level = "trusted".
    hdr = re.search(r'(?m)^\s*\[projects\.\"' + re.escape(rp) + r'\"\]\s*$', text)
    if hdr:
        rest = text[hdr.end():]
        nxt = re.search(r'(?m)^\s*\[', rest)
        block = rest[: nxt.start()] if nxt else rest
        if re.search(r'(?m)^\s*trust_level\s*=\s*"trusted"', block):
            status["project_trusted"] = True
    return status


def codex_is_interactive(status: dict) -> bool:
    pol = status.get("approval_policy")
    # Default (no config / unset) is interactive `on-request`. `never` is the only non-prompting mode.
    if pol is None:
        return True
    return pol in ("untrusted", "on-request", "on-failure")


def codex_friction_signal(root: Path) -> dict | None:
    """The canonical `permission-friction` signal for Codex: an interactive, untrusted workspace
    keeps pausing for approval. Trust (or `approval_policy="never"`) clears it."""
    status = codex_permission_status(root)
    if status["project_trusted"]:
        return None
    if not codex_is_interactive(status):
        return None
    return {"signal": "permission-friction", "approval_policy": status["approval_policy"] or "on-request",
            "config": status["config"]}


def render_permission_hint(root: Path, status: dict) -> str:
    rp = str(root.resolve())
    return (
        "🏹 Celeborn advisor —> Codex still pauses for approval in this workspace "
        f"(approval_policy={status.get('approval_policy') or 'on-request'}, "
        f"trusted={status['project_trusted']}). Trust it once so Codex stops re-prompting — add to "
        f"{status['config']}:\n\n"
        f'    [projects."{rp}"]\n'
        f'    trust_level = "trusted"\n\n'
        "(or set `approval_policy = \"never\"` + `sandbox_mode = \"workspace-write\"` globally). "
        "Codex's lever is coarse — a workspace trust flag, not per-command rules like Claude Code."
    )


def cmd_permissions(args: argparse.Namespace) -> int:
    """Codex permission lever (the `permissions` analog of `celeborn permissions`). `--suggest`
    (default, read-only) prints the config.toml trust/approval hint; `--apply` appends the
    `[projects."<root>"]` trust block when it is absent (idempotent, never edits in place)."""
    root = Path(args.path or ".").resolve()
    ctx_root = find_context_root(root) or root
    status = codex_permission_status(ctx_root)
    if status["project_trusted"]:
        print(f"✓ Codex already trusts {ctx_root} (no approval friction). config: {status['config']}")
        return 0
    if not codex_is_interactive(status):
        print(f"✓ Codex approval_policy={status['approval_policy']} — non-interactive, no friction. "
              f"config: {status['config']}")
        return 0

    print(render_permission_hint(ctx_root, status))
    if not getattr(args, "apply", False):
        print(f"\nApply (append the trust block):  codex_celeborn.py permissions --apply --path {ctx_root}")
        return 0

    cfg = Path(status["config"])
    rp = str(ctx_root.resolve())
    header = f'[projects."{rp}"]'
    existing = cfg.read_text(encoding="utf-8") if cfg.is_file() else ""
    if header in existing:
        print(f"\n⚠ {header} already present in {cfg} — edit `trust_level` by hand to avoid a duplicate table.")
        return 1
    cfg.parent.mkdir(parents=True, exist_ok=True)
    sep = "" if (not existing or existing.endswith("\n\n")) else ("\n" if existing.endswith("\n") else "\n\n")
    with cfg.open("a", encoding="utf-8") as fh:
        fh.write(f'{sep}{header}\ntrust_level = "trusted"\n')
    print(f"\n✓ Appended trust block to {cfg} — Codex will stop pausing for approval in {ctx_root}.")
    return 0


def cmd_advise(args: argparse.Namespace) -> int:
    """Codex friction advisor (the `celeborn advise` analog). Prints the recommendation that applies
    right now, or a clean line when there is none. `--json` for the structured form."""
    root = Path(args.path or ".").resolve()
    ctx_root = find_context_root(root) or root
    sig = codex_friction_signal(ctx_root)
    recs = []
    if sig:
        status = codex_permission_status(ctx_root)
        recs.append({"intent": "reduce-permission-friction", "signal": sig,
                     "text": render_permission_hint(ctx_root, status), "channel": "config"})
    if getattr(args, "json", False):
        print(json.dumps({"harness": "codex", "recommendations": recs}, indent=2))
        return 0
    if not recs:
        print("✓ No Codex friction detected — nothing to recommend right now.")
        return 0
    print(f"🏹 Celeborn advisor (codex) — {len(recs)} recommendation(s):")
    for r in recs:
        print(f"  • [{r['intent']}] {r['text']}")
    return 0


# --------------------------------------------------------------------------- hooks

def hook_session_start(payload: dict) -> int:
    root = workspace_root(payload)
    sid = session_id(payload)
    ctx_root = find_context_root(root)
    if ctx_root is None:
        return 0

    ensure_agents_md(ctx_root)

    if sid:
        run_celeborn("record", "orient", "--session", sid, project=ctx_root)

    result = run_celeborn("status", project=ctx_root)
    status = (result.stdout or "").strip()
    if not status:
        return 0

    # Carry the advisor recommendation on the SAME injection channel as orient (plan deliverable #6:
    # the pending-file injection carries an advisor notice as well as the Hot tier).
    advice = ""
    sig = codex_friction_signal(ctx_root)
    if sig:
        advice = "\n\n## Celeborn advisor\n\n" + render_permission_hint(ctx_root, codex_permission_status(ctx_root))

    pending = ctx_root / ".context" / ORIENT_FILE
    pending.write_text(
        "# Celeborn Orient (Codex session start)\n\n"
        f"**Project root:** `{ctx_root}`\n\n"
        "Codex does not inject SessionStart hook stdout into the model. "
        "Read this file once, orient from it, then delete it.\n\n"
        "Kanban shorthand in this project: `wire tN` / `claim tN` = take card tN; "
        "`ship tN` = close it; `hydrate` = you are here.\n\n" + status + advice + "\n",
        encoding="utf-8",
    )
    print("## Celeborn memory (Orient load)\n")
    print(status)
    if advice:
        print(advice)
    return 0


def hook_user_prompt_submit(payload: dict) -> int:
    root = workspace_root(payload)
    sid = session_id(payload)
    ctx_root = find_context_root(root)
    if ctx_root is None:
        return 0

    rollout = find_rollout_file(sid, root)
    tokens = read_context_tokens(rollout)
    lines = []

    pending = ctx_root / ".context" / ORIENT_FILE
    if pending.is_file():
        lines.append(ORIENT_NUDGE.format(root=ctx_root))

    if tokens is not None:
        remind = run_celeborn(
            "remind",
            "--tokens", str(tokens),
            "--every", "50000",
            "--soft-limit", "150000",
            "--alarm-limit", "200000",
            "--alarm-every", "100000",
            "--clear-cmd", CODEX_RENEW_CMD,
            project=ctx_root,
        )
        msg = (remind.stdout or remind.stderr or "").strip()
        if msg:
            lines.append(msg)

    if sid:
        beat = run_celeborn("heartbeat", "--session", sid, project=ctx_root)
        beat_line = (beat.stdout or "").strip()
        if beat_line:
            lines.append(beat_line)

    if rollout is not None and rollout.is_file():
        dest = claude_transcript_path(sid or "session")
        convert_codex_transcript(rollout, sid or "session", dest)
        if tokens is not None:
            run_celeborn("record", "turn", "--tokens", str(tokens), project=ctx_root)

    if lines:
        print("\n".join(lines))
    return 0


def hook_stop(payload: dict) -> int:
    root = workspace_root(payload)
    sid = session_id(payload) or "session"
    rollout = find_rollout_file(sid if sid != "session" else session_id(payload), root)
    if rollout is None or not rollout.is_file():
        return 0

    dest = claude_transcript_path(sid)
    if convert_codex_transcript(rollout, sid, dest) == 0:
        return 0

    ctx_root = find_context_root(root)
    cap_args = ["capture", "--transcript", str(dest), "--session", sid, "--quiet", "--note"]
    if ctx_root is None:
        cap_args.append("--global")
    capture = run_celeborn(*cap_args, project=ctx_root)
    note = (capture.stdout or "").strip()
    if note:
        if note.startswith("{"):
            try:
                env = json.loads(note)
                msg = env.get("systemMessage")
                if msg:
                    print(msg)
                    return 0
            except json.JSONDecodeError:
                pass
        print(note)
    return 0


def hook_pre_compact(payload: dict) -> int:
    root = workspace_root(payload)
    ctx_root = find_context_root(root)
    if ctx_root is not None:
        run_celeborn("record", "compaction", project=ctx_root)
    print(PRECOMPACT_MSG)
    return 0


def hook_session_end(payload: dict) -> int:
    root = workspace_root(payload)
    ctx_root = find_context_root(root)
    if ctx_root is None:
        return 0
    run_celeborn("handoff", project=ctx_root)
    pending = ctx_root / ".context" / ORIENT_FILE
    if pending.is_file():
        pending.unlink(missing_ok=True)
    return 0


def hook_notify(payload: dict) -> int:
    """Codex's legacy `notify` program hook (fires once on `agent-turn-complete`). We treat it as a
    Stop equivalent: convert + capture the just-completed turn. argv may carry the JSON as the last
    arg rather than on stdin (see legacy_notify)."""
    if not payload:
        # legacy_notify appends the JSON as the final argv item.
        for arg in reversed(sys.argv):
            if arg.strip().startswith("{"):
                try:
                    payload = json.loads(arg)
                    break
                except json.JSONDecodeError:
                    continue
    if payload.get("type") not in (None, "agent-turn-complete"):
        return 0
    return hook_stop(payload)


