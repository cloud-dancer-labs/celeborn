#!/usr/bin/env python3
"""Codex CLI adapter for Celeborn — bridges Codex hooks/rollouts to the stock CLI.

Mirrors the Grok bridge (`grok/scripts/grok_celeborn.py`) and does **not** modify Celeborn
core. It converts Codex rollout session files (`~/.codex/sessions/YYYY/MM/DD/rollout-*.jsonl`)
into the Claude Code JSONL shape that `celeborn capture` expects, normalizes Codex's
tool/function vocabulary into the canonical names, and dispatches hook events without
`$CELEBORN_HOME` or `hookSpecificOutput`.

The four adapter-contract responsibilities (see plan/t70-skill-advisor.md) are fulfilled here,
at the bridge level, exactly as the Grok bridge fulfills them:

  * friction_signals  — `cmd_advise` reads ~/.codex/config.toml and flags an interactive,
                         untrusted workspace as canonical `permission-friction`.
  * permission_target — `~/.codex/config.toml` (coarse approval/sandbox + per-project trust),
                         not a per-command allow-list.
  * render            — Codex idiom: a config.toml trust/approval hint (cmd_permissions).
  * inject            — AGENTS.md managed block + `.codex-orient-pending.md` (Codex auto-loads
                         AGENTS.md every session; it does not inject SessionStart stdout).
"""
